#!/bin/bash
set -euo pipefail

# Calciforge Binary Installer
# Builds, deploys, and configures the calciforge binary
#
# Usage:
#   scripts/install-calciforge.sh <action> [host1 host2 ...]
#
# Actions:
#   build     Build release binary
#   deploy    Deploy binary + config + service
#   install   Build + deploy + verify (full setup)
#   verify    Check service health
#   restart   Restart service
#   status    Show service status
#
# Examples:
#   scripts/install-calciforge.sh install 192.168.1.127
#   scripts/install-calciforge.sh install 192.168.1.127 192.168.1.49
#   scripts/install-calciforge.sh deploy 192.168.1.127
#   scripts/install-calciforge.sh verify 192.168.1.127

PROJECT_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
INSTALL_DIR="/opt/calciforge"
CONFIG_DIR="/etc/calciforge"
SSH_KEY="${SSH_KEY:-$HOME/.ssh/id_ed25519}"

# ── Target resolution ──────────────────────────────────────────────
ACTION="${1:-help}"
shift || true

if [ $# -gt 0 ]; then
    TARGETS=("$@")
elif [ -f "$(dirname "$0")/targets.txt" ]; then
    mapfile -t TARGETS < <(grep -v '^#' "$(dirname "$0")/targets.txt" | grep -v '^$')
else
    echo "No targets specified. Usage:"
    echo "  $0 <action> host1 [host2 ...]"
    exit 1
fi

echo "=== Calciforge Installer ==="
echo "Action: $ACTION"
echo "Targets: ${TARGETS[*]}"
echo ""

# ── Helpers ────────────────────────────────────────────────────────
run_on() {
    local host="$1"; shift
    ssh -i "$SSH_KEY" -o StrictHostKeyChecking=accept-new -o ConnectTimeout=10 "root@$host" "$@"
}

copy_to() {
    local src="$1" host="$2" dst="$3"
    scp -i "$SSH_KEY" -o StrictHostKeyChecking=accept-new "$src" "root@$host:$dst"
}

